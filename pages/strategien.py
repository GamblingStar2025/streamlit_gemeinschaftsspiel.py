
import streamlit as st

st.title("💡 Strategien & Methoden")

st.markdown("""
### 🔥 Hot-Zahlen
Zahlen, die in den letzten Ziehungen besonders häufig aufgetreten sind.

### ❄️ Cold-Zahlen
Zahlen, die schon lange nicht mehr gezogen wurden – mögliche Ausgleichskandidaten.

### ♻️ Rad-Prinzip (Zyklische Wiederkehr)
Sucht nach Mustern von Zahlen, die in bestimmten Zeitabständen erneut auftauchen.

### 🎲 Monte-Carlo-Simulation
Zufallssimulation von Ziehungen – häufig gezogene Kombinationen werden favorisiert.

### 🧠 LSTM-Vorhersage (KI-Modell)
Ein neuronales Netzwerk, das historische Ziehungen lernt und künftige Muster ableitet.

### 🧩 Clusteranalyse
Zahlenbereiche (z. B. 1–10, 11–20…) werden nach Häufung untersucht.

---

**💡 Hinweis:** Die Gewichtung dieser Strategien kann unter „Tipp-Generator“ individuell angepasst werden.
""")
